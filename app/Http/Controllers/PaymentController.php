     */
    public function update(PaymentRequest $request)
    {
        $account = Account::where('id', $request->id)->first();
        $account->update([
            'surplus' => $account->surplus - $request->amount
        ]);

        TransactionHistory::create([
            'account_id' => $request->id,
            'amount' => $request->amount,
            'created_at' => now(),
        ]);
    }

